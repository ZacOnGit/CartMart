    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String newJson = "[\n";
        for(int i = 0; i < inventory.getCount(); i++){
            Item item = inventory.getItem(i);
            newJson += " {\n   \"itemID\": " + item.getItemId() + ",\n";
            newJson += "   \"itemName\": \"" + item.getItemName() + "\",\n";
            newJson += "   \"itemCategory\": \"" + item.getItemCategory() + "\",\n";
            newJson += "   \"itemPrice\": " + item.getItemPrice() + ",\n";
            newJson += "   \"itemQty\": " + item.getItemQuantity() + ",\n";
            newJson += "   \"itemDesc\": \"" + item.getItemDescription() + "\",\n";
            newJson += "   \"itemImage\": " + item.getItemImage() + ",\n";
            if(i == inventory.getCount()-1){
                newJson+= " }\n";
            } else {
                 newJson += " },\n";
            }
        }
        newJson += "]";
        try {
            //        File file = new File("inventoryList.json");
            FileWriter file = new FileWriter("inventoryListTest.json");
            file.write(newJson);
            file.close();
        } catch (IOException ex) {
            Logger.getLogger(ManagerView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

